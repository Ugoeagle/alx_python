import random

# Assign a random signed number to the variable 'number' each time the program is executed
number = random.randint(-100, 100)

# Print whether the number is positive, zero, or negative
print("{} is" .format(number), end=" ")
if number > 0:
    print("positive")
elif number == 0:
    print("zero")
else:
    print("negative")















    
    
      
    